      integer :: isp, izp, jsp, jzp

      ksig(:, :) = 0.0_wp
      kpi(:, :) = 0.0_wp
      kdel(:, :) = 0.0_wp

      do isp = 1, mol%nid
         izp = mol%num(isp)
         do jsp = 1, mol%nid
