            ! Geometric mean
            ksig(isp, jsp) = (p_ceh_h0k(1,izp) * p_ceh_h0k(1,jzp))**0.5_wp 
            kpi (isp, jsp) = (p_ceh_h0k(2,izp) * p_ceh_h0k(2,jzp))**0.5_wp 
            kdel(isp, jsp) = (p_ceh_h0k(3,izp) * p_ceh_h0k(3,jzp))**0.5_wp 
         end do
      end do

   end subroutine get_diat_scale


